package com.glitterlabs.videoqnaapp.adaptor;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import com.glitterlabs.videoqnaapp.fragment.BusinessUserFragment;
import com.glitterlabs.videoqnaapp.fragment.ChatHistoryFragment;

//Extending FragmentStatePagerAdapter
public class Pager extends FragmentStatePagerAdapter {

    //integer to count number of tabs
    int tabCount;

    //Constructor to the class
    public Pager(FragmentManager fm, int tabCount) {
        super(fm);
        //Initializing tab count
        this.tabCount= tabCount;
    }

    //Overriding method getItem
    @Override
    public Fragment getItem(int position) {
        //Returning the current tabs
        switch (position) {
            case 0:
                BusinessUserFragment businessFragment = new BusinessUserFragment();
                return businessFragment;
            case 1:
                ChatHistoryFragment chatFragment = new ChatHistoryFragment();
                return chatFragment;

            default:
                return null;
        }
    }

    //Overriden method getCount to get the number of tabs
    @Override
    public int getCount() {
        return tabCount;
    }
}
